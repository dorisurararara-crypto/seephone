def main():
    d = json.load(open(PATH, encoding='utf-8'))
    keys_before = set(d.keys())
    changed = 0
    for celeb, sec_map in POLISH.items():
        if celeb not in d:
            print(f'ERROR: {celeb} 없음', file=sys.stderr)
            sys.exit(1)
        existing = {s['id']: s for s in d[celeb]['sections']}
        for sid, body in sec_map.items():
            if sid not in existing:
                print(f'ERROR: {celeb}.{sid} 섹션 없음', file=sys.stderr)
                sys.exit(1)
            existing[sid]['bodyKo'] = body
            changed += 1
        total = sum(len(s['bodyKo']) for s in d[celeb]['sections'])
        print(f'{celeb}: {len(d[celeb]["sections"])} 섹션 / {total}자')
        if not (900 <= total <= 1300):
            print(f'WARN: {celeb} 글자수 범위 밖 ({total})', file=sys.stderr)
            sys.exit(1)
    keys_after = set(d.keys())
    assert keys_before == keys_after, 'keys 변동'
    assert len(d) == 31, f'key 수 {len(d)} != 31'
    with open(PATH, 'w', encoding='utf-8') as f:
        json.dump(d, f, ensure_ascii=False, indent=2)
        f.write('\n')
    print(f'OK — {changed} 섹션 polish / {len(d)} keys 보존')


if __name__ == '__main__':
    main()
